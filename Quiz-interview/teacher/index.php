    // Update the pending requests count when the page loads
    $(document).ready(function() {
      updatePendingRequestsCount();

      // Attach a click event to the notification icon
      $(".notification-icon").click(function() {
        toggleNotificationCount();
      });
    });

    // Function to toggle the visibility of the pending requests count
    function toggleNotificationCount() {
      var pendingRequestsElement = $("#pendingRequests");

      // Toggle the display property
      pendingRequestsElement.toggle();
    }

    // Function to update the pending requests count
    function updatePendingRequestsCount() {
      var notificationIcon = $(".notification-icon");
      var pendingRequestsElement = $("#pendingRequests");

      // Get the value of the data-pending-requests attribute
      var pendingRequestsCount = parseInt(notificationIcon.data("pending-requests")) || 0;

      // Update the count
      pendingRequestsElement.text(pendingRequestsCount);

      // Show/hide the count based on whether there are pending requests
      pendingRequestsElement.css("display", pendingRequestsCount > 0 ? "inline" : "none");
    }
  </script>
